class Solution
{
  public:
    bool same(Node* a,Node* b){
        if(!a&&!b)                  //Reaching the leaf nodes for both tree
        return true;
        if(!a||!b)                 //Reaching leaf node for anyone of the tree 
        return false;
        return (a->data==b->data && same(a->right,b->right) && same(a->left,b->left));
        //recursiveley calling "same" function and checking the corresponding nodes
    }
    //Function to check if S is a subtree of tree T.
    bool isSubTree(Node* T, Node* S) 
    {
        // Your code here
        if(!S)                  //If subtree is null then the answer will always be true
        return true;
        //If the compiler comes here means S is not null
        if(!T)                  //If T is null with S being not null the answer will always be false
        return false;
        if(same(T,S))           //checking the equality of trees 
        return true;
        //If the trees are not same then we will start traversing left and right subtrees of T
        return isSubTree(T->left,S)||isSubTree(T->right,S);
        
    }
};

